    private void jbttn_filtrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbttn_filtrarMouseClicked
       tabla.setRowCount(0);
       String filtro = String.valueOf(Cbox_filtro.getSelectedItem());
       
        // ESTABLECER LOS TITULOS Y EL RESTO DEL FORMATO DE LA TABLA
        String titulos[] = {"ID", "Titulo", "Descripción", "Estado"};
        tabla.setColumnIdentifiers(titulos);

        List<ListaTareas> listaTarea = ctrlLogLis.MostrarDatos();

        if (listaTarea != null) {
            for (ListaTareas tare : listaTarea) {
                // Utiliza equalsIgnoreCase para comparar ignorando mayúsculas/minúsculas
                if (tare.getEstado().equals(filtro)) {
                    Object[] objeto = {
                            tare.getId(),
                            tare.getTitulo(),
                            tare.getDescripcion(),
                            tare.getEstado()
                    };

                    tabla.addRow(objeto);
                } 
                
            }
            
        }
        jtbl_ver.setModel(tabla);
        
    }//GEN-LAST:event_jbttn_filtrarMouseClicked

