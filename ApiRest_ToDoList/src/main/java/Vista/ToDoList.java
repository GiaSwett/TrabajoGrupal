    public int buscarId(String titulo) {
        
       int count = 0;
       List<ListaTareas> tareas = ctrlLogLis.ConsultarTarea();
       for (ListaTareas tare : tareas) {
           if (tare.getTitulo().equals(titulo)) {
                return count = tare.getId();
           }
       }
        return count;
    }
    
